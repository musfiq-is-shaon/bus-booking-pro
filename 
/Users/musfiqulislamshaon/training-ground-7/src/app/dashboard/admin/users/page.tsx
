'use client';

import { useEffect, useState } from 'react';
import { createClient } from '@/lib/supabase/client';
import { Users, Search, Crown, Shield, User, Mail, Calendar, ToggleLeft, ToggleRight } from 'lucide-react';
import { formatDate } from '@/lib/utils';

interface Profile {
  id: string;
  email: string;
  full_name: string;
  phone: string;
  role: 'user' | 'admin';
  created_at: string;
}

export default function AdminUsersPage() {
  const supabase = createClient();
  const [users, setUsers] = useState<Profile[]>([]);
  const [loading, setLoading] = useState(true);
  const [searchQuery, setSearchQuery] = useState('');
  const [roleFilter, setRoleFilter] = useState('all');

  useEffect(() => {
    fetchUsers();
  }, []);

  const fetchUsers = async () => {
    const { data, error } = await supabase
      .from('profiles')
      .select('*')
      .order('created_at', { ascending: false });

    if (!error) {
      setUsers(data || []);
    }
    setLoading(false);
  };

  const handleRoleChange = async (userId: string, newRole: 'user' | 'admin') => {
    try {
      const { error } = await supabase
        .from('profiles')
        .update({ role: newRole })
        .eq('id', userId);

      if (error) throw error;
      fetchUsers();
    } catch (err: any) {
      console.error('Error updating role:', err);
    }
  };

  const filteredUsers = users.filter(user => {
    const matchesSearch = !searchQuery ||
      user.email.toLowerCase().includes(searchQuery.toLowerCase()) ||
      user.full_name?.toLowerCase().includes(searchQuery.toLowerCase());
    
    const matchesRole = roleFilter === 'all' || user.role === roleFilter;

    return matchesSearch && matchesRole;
  });

  if (loading) {
    return (
      <div className="p-6">
        <div className="spinner w-8 h-8 border-4 mx-auto" />
      </div>
    );
  }

  return (
    <div className="p-6">
      {/* Header */}
      <div className="mb-6">
        <h1 className="text-2xl font-bold text-secondary-900">Manage Users</h1>
        <p className="text-secondary-500">View and manage all registered users</p>
      </div>

      {/* Filters */}
      <div className="card p-4 mb-6">
        <div className="grid sm:grid-cols-2 gap-4">
          <div className="relative">
            <Search className="absolute left-4 top-1/2 -translate-y-1/2 w-5 h-5 text-secondary-400" />
            <input
              type="text"
              placeholder="Search users..."
              value={searchQuery}
              onChange={(e) => setSearchQuery(e.target.value)}
              className="input pl-12"
            />
          </div>
          <select
            value={roleFilter}
            onChange={(e) => setRoleFilter(e.target.value)}
            className="input"
          >
            <option value="all">All Roles</option>
            <option value="admin">Admins</option>
            <option value="user">Users</option>
          </select>
        </div>
      </div>

      {/* Stats */}
      <div className="grid grid-cols-3 gap-4 mb-6">
        <div className="card p-4 text-center">
          <p className="text-2xl font-bold text-secondary-900">{users.length}</p>
          <p className="text-sm text-secondary-500">Total Users</p>
        </div>
        <div className="card p-4 text-center">
          <p className="text-2xl font-bold text-primary-600">{users.filter(u => u.role === 'admin').length}</p>
          <p className="text-sm text-secondary-500">Admins</p>
        </div>
        <div className="card p-4 text-center">
          <p className="text-2xl font-bold text-secondary-700">{users.filter(u => u.role === 'user').length}</p>
          <p className="text-sm text-secondary-500">Regular Users</p>
        </div>
      </div>

      {/* Users Table */}
      <div className="card overflow-hidden">
        <div className="overflow-x-auto">
          <table className="w-full">
            <thead className="bg-secondary-50">
              <tr>
                <th className="px-6 py-3 text-left text-xs font-medium text-secondary-500 uppercase">User</th>
                <th className="px-6 py-3 text-left text-xs font-medium text-secondary-500 uppercase">Email</th>
                <th className="px-6 py-3 text-left text-xs font-medium text-secondary-500 uppercase">Phone</th>
                <th className="px-6 py-3 text-left text-xs font-medium text-secondary-500 uppercase">Joined</th>
                <th className="px-6 py-3 text-left text-xs font-medium text-secondary-500 uppercase">Role</th>
                <th className="px-6 py-3 text-left text-xs font-medium text-secondary-500 uppercase">Actions</th>
              </tr>
            </thead>
            <tbody className="divide-y divide-secondary-100">
              {filteredUsers.map((user) => (
                <tr key={user.id} className="hover:bg-secondary-50">
                  <td className="px-6 py-4">
                    <div className="flex items-center gap-3">
                      <div className="w-10 h-10 bg-primary-100 rounded-full flex items-center justify-center">
                        <span className="text-primary-700 font-semibold">
                          {user.full_name?.charAt(0) || user.email.charAt(0).toUpperCase()}
                        </span>
                      </div>
                      <span className="font-medium text-secondary-900">
                        {user.full_name || 'Unnamed User'}
                      </span>
                    </div>
                  </td>
                  <td className="px-6 py-4 text-sm text-secondary-600">
                    <div className="flex items-center gap-2">
                      <Mail className="w-4 h-4 text-secondary-400" />
                      {user.email}
                    </div>
                  </td>
                  <td className="px-6 py-4 text-sm text-secondary-600">
                    {user.phone || 'N/A'}
                  </td>
                  <td className="px-6 py-4 text-sm text-secondary-600">
                    <div className="flex items-center gap-2">
                      <Calendar className="w-4 h-4 text-secondary-400" />
                      {formatDate(user.created_at)}
                    </div>
                  </td>
                  <td className="px-6 py-4">
                    <span className={`badge ${user.role === 'admin' ? 'badge-primary' : 'bg-secondary-100 text-secondary-700'}`}>
                      {user.role === 'admin' ? (
                        <><Crown className="w-3 h-3 mr-1" /> Admin</>
                      ) : (
                        <><User className="w-3 h-3 mr-1" /> User</>
                      )}
                    </span>
                  </td>
                  <td className="px-6 py-4">
                    <button
                      onClick={() => handleRoleChange(user.id, user.role === 'admin' ? 'user' : 'admin')}
                      className={`flex items-center gap-2 px-3 py-1.5 rounded-lg text-sm font-medium transition-colors ${
                        user.role === 'admin'
                          ? 'bg-secondary-100 text-secondary-600 hover:bg-secondary-200'
                          : 'bg-primary-50 text-primary-600 hover:bg-primary-100'
                      }`}
                    >
                      {user.role === 'admin' ? (
                        <>
                          <ToggleRight className="w-4 h-4" />
                          Remove Admin
                        </>
                      ) : (
                        <>
                          <Shield className="w-4 h-4" />
                          Make Admin
                        </>
                      )}
                    </button>
                  </td>
                </tr>
              ))}
            </tbody>
          </table>
        </div>

        {filteredUsers.length === 0 && (
          <div className="p-12 text-center">
            <Users className="w-16 h-16 text-secondary-300 mx-auto mb-4" />
            <h3 className="text-lg font-semibold text-secondary-900 mb-2">No users found</h3>
            <p className="text-secondary-500">Try adjusting your search criteria</p>
          </div>
        )}
      </div>
    </div>
  );
}

